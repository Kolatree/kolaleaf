// OpenAPIContractTests.swift  (Phase 0 · U84)
//
// Contract test: load the backend's OpenAPI 3.1 snapshot and assert each
// curated iOS Codable DTO can decode a payload synthesised from its
// corresponding schema's `required` fields.
//
// If the backend adds a non-optional response field that the matching
// Swift DTO doesn't model, the synthesised payload includes the new key
// and `JSONDecoder.decode` ignores it cleanly — so missing-on-iOS
// detection isn't the main signal here. The main signal is the inverse:
// if the iOS DTO declares a NON-OPTIONAL property that the OpenAPI
// `required[]` list no longer contains, the synthesised payload omits
// the key and Swift's decode throws `.keyNotFound`. Tests catch this at
// CI time instead of runtime in production.
//
// The snapshot is regenerated by `npm run openapi:snapshot` (see
// `scripts/export-openapi-snapshot.ts`). Run it whenever the backend
// schemas change; commit the resulting `openapi-snapshot.json`. CI
// should also run the script and `git diff --exit-code` the result to
// catch a backend schema change that landed without snapshot refresh.
//
// Plan reference: docs/plans/2026-05-09-001-feat-ios-swiftui-kolaleaf-mobile-app-plan.md
// (lines 2268–2279).

